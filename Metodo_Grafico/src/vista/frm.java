    public void initModel(double coefX, double coefY, String tipoF, boolean valorNegativo) {
        cs = new Solucion(coefX, coefY, valorNegativo, tipoF, txtAreaResultados);
        for (int i = 0; i < this.jPanel5.getComponentCount(); i++) {
            OpcionesRestric p = (OpcionesRestric) this.jPanel5.getComponent(i);
            cs.adicionarRestriccion(new Restriccion(i + 1, p.getX1(), p.getX2(), p.getSignoCoeficiente(), p.getTipoRestriccion()));
        }
        cs.mostrarEnTxtArea(cs.toString());
        PuntoSolucion p = cs.puntoIdeal();
        cs.mostrarEnTxtArea(String.valueOf(p));
    }

