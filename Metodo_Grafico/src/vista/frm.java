    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JTextArea textArea = new JTextArea();
        try{
            this.initModel(Double.parseDouble(this.jFormattedTextField1.getText().replace(',', '.')), Double.parseDouble(this.jFormattedTextField2.getText().replace(',', '.')), this.jComboBox1.getSelectedItem().toString(), !this.jCheckBox1.isSelected());
            this.graphCanvas1.traerSolucion(cs);
            this.graphCanvas1.repaint();
            PuntoSolucion p=cs.puntoIdeal();
            if(p!=null){
                if(cs.tipoF.equals(Solucion.maximizar)){
                    jTextArea1.setText(jTextArea1.getText() + "PUNTO MAX: ("+p.x+","+p.y+")");
                   
                    textArea.updateUI();
                    //JOptionPane.showMessageDialog(this,"PUNTO MAX: ("+p.x+","+p.y+")");
                }else{
                     jTextArea1.setText(jTextArea1.getText() + "PUNTO MIN: ("+p.x+","+p.y+")");
                    textArea.updateUI();
                    //JOptionPane.showMessageDialog(this,"PUNTO MIN: ("+p.x+","+p.y+")");
                }
            }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        
    
    }//GEN-LAST:event_jButton3ActionPerformed

